export function sum (a:number , b:number){
    return a + b
}
export function subtract (a:number , b:number){
    return a - b 
}
export function product (a:number , b:number){
    return a * b 
}
export function divison (a:number , b:number){
    return a / b 
}
export function square (a:number){
    return Math.sqrt(a)
}
export function logarithm(a:number){
    return Math.log(a)
}